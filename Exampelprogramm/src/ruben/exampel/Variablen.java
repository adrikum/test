package ruben.exampel;



public class Variablen
{



	public static void main(String[] args)
	{
		
		/* Variablen sind kleine Container, die Daten zum nutzen bereit halten.
		 * Jede Variable besteht aus drei Teilen:
		 *  - Typ den die Variable speichern kann (int, double, etc)
		 *  - Name mit dem auf sie zugegriffen werden kann
		 *  - Inhalt der gespeichert wird
		 *  
		 *  Typ   Name   Inahlt
		 *  int   zahl   = 3;
		 */
	
		int a = 1;	// Ganzzahlen
		
		double b = 0.5;	// Kommazahlen
		
		boolean c = false;	// Richtig/Falsch
		
		String d = "hallo";	// Buchstabenketten
	
	}



}
